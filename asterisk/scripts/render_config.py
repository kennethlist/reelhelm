#!/usr/bin/env python3
"""Render Asterisk .conf files from the data/config.json file.

Asterisk has no native JSON support; it reads .conf files. This script is run
by the container entrypoint at startup. It reads the user-facing config.json,
validates it, and writes pjsip.conf / extensions.conf / voicemail.conf /
rtp.conf / asterisk.conf / logger.conf into the Asterisk config directory.

Usage: render_config.py <config.json> <output_dir>
"""

import json
import sys
import urllib.request


def fail(msg):
    print(f"[render_config] ERROR: {msg}", file=sys.stderr)
    sys.exit(1)


def detect_external_ip():
    for url in ("https://api.ipify.org", "https://ifconfig.me/ip"):
        try:
            with urllib.request.urlopen(url, timeout=5) as r:
                ip = r.read().decode().strip()
                if ip:
                    return ip
        except Exception:
            continue
    return None


