# Validation
# ---------------------------------------------------------------------------

def load_and_validate(path):
    try:
        with open(path) as f:
            cfg = json.load(f)
    except FileNotFoundError:
        fail(f"config file not found: {path}")
    except json.JSONDecodeError as e:
        fail(f"config.json is not valid JSON: {e}")

    trunk = cfg.get("trunk")
    if not trunk:
        fail("missing 'trunk' section")
    for key in ("server", "username", "password", "did"):
        if not trunk.get(key):
            fail(f"trunk.{key} is required")

    if trunk.get("encryption"):
        host = str(trunk["server"]).split(".")[0]
        if not host[-1:].isdigit():
            print(f"[render_config] WARNING: trunk.encryption is on but server "
                  f"'{trunk['server']}' is not a numbered host. Some SIP trunk "
                  f"providers require a numbered hostname for TLS (e.g. sip1.example.com), "
                  f"or registration will fail.", file=sys.stderr)

    exts = cfg.get("extensions")
    if not exts:
        fail("at least one entry in 'extensions' is required")
    seen = set()
    for e in exts:
        eid = str(e.get("id", "")).strip()
        if not eid:
            fail("every extension needs an 'id'")
        if not e.get("password"):
            fail(f"extension {eid} needs a 'password'")
        if eid in seen:
            fail(f"duplicate extension id: {eid}")
        seen.add(eid)

    inbound = str(cfg.get("inbound_extension") or exts[0]["id"])
    if inbound not in seen:
        fail(f"inbound_extension '{inbound}' is not a defined extension")
    cfg["inbound_extension"] = inbound

    cfg.setdefault("blocklist", [])
    cfg.setdefault("sms", {"enabled": False})
    lg = cfg.setdefault("logging", {})
    lg.setdefault("calls", True)
    lg.setdefault("messages", True)
    s = cfg.setdefault("settings", {})
    s.setdefault("codecs", ["ulaw", "alaw"])
    s.setdefault("rtp_start", 10000)
    s.setdefault("rtp_end", 10100)
    s.setdefault("external_ip", "auto")
    s.setdefault("timezone", "America/New_York")
    # Encrypt the client/extension leg too (SIP-TLS on 5061 + mandatory SRTP).
    # Independent of trunk.encryption, which only covers the trunk leg.
    s.setdefault("client_encryption", False)
    return cfg


